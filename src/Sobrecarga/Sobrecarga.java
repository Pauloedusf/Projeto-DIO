package Sobrecarga;

public class Sobrecarga {
	
	
	
	
	
	public static double CalculaQuadrado(double altura, double largura) {
		return altura * largura;
	}
	
	public static double CalculaRetangulo(double altura, double largura) {
		return altura * largura;
	}
	
	public static double CalculaTriangulo(double altura, double largura) {
		return (altura * largura) / 2;
	}
	
	

}
